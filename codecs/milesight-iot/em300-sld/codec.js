// Normalized payload codec for Milesight EM300-SLD / EM300-ZLD (Spot/Rope
// Leakage Detection Sensor: liquid-leak detection + temperature + humidity).
//
// Original work for @intelligent-farming/lorawan-codec-normalization. Wire
// format (Milesight channel/type TLV) understood with reference to the upstream
// Apache-2.0 decoder (TheThingsNetwork/lorawan-devices
// vendor/milesight-iot/em300-sld.js, attributed in NOTICE). Author the
// normalization here; do NOT copy upstream normalizeUplink/normalizedOutput.
//
// Ported from upstream Decoder() (Copyright 2021 Milesight IoT, Apache-2.0).
//
// Channel/type mapping (upstream is the source of truth for the wire format):
//   0x01/0x75 battery      byte %                  -> batteryPercent extra
//   0x03/0x67 temperature  int16 LE /10 °C         -> air.temperature
//   0x04/0x68 humidity     byte /2 %               -> air.relativeHumidity
//   0x05/0x00 water leak   status byte (0=normal)  -> water.leak (boolean)
//
// Milesight reports battery as a PERCENTAGE; the vocabulary's `battery` is
// volts, so the percentage is emitted as the camelCase extra `batteryPercent`
// rather than being forced into a volts field. Upstream emits the leak status
// as the string "normal"/"leak"; the vocabulary's `water.leak` is a boolean,
// so it is normalized to true (leak) / false (normal).

function round(value, decimals) {
  var f = Math.pow(10, decimals);
  return Math.round(value * f) / f;
}

function u16le(lo, hi) {
  return ((hi << 8) | lo) & 0xffff;
}

function s16le(lo, hi) {
  var v = u16le(lo, hi);
  return v > 0x7fff ? v - 0x10000 : v;
}

function decodeUplinkCore(input) {
  var bytes = input.bytes;
  if (!bytes || bytes.length === 0) {
    return { errors: ['empty payload'] };
  }

  var data = {};
  var air = {};
  var water = {};
  var hasAir = false;
  var hasWater = false;
  var recognized = false;

  var i = 0;
  while (i + 1 < bytes.length) {
    var channel = bytes[i];
    var type = bytes[i + 1];

    if (channel === 0x01 && type === 0x75) {
      // BATTERY: percentage
      data.batteryPercent = bytes[i + 2];
      i += 3;
      recognized = true;
    } else if (channel === 0x03 && type === 0x67) {
      // TEMPERATURE: int16 LE, 0.1 °C resolution
      air.temperature = round(s16le(bytes[i + 2], bytes[i + 3]) / 10, 1);
      hasAir = true;
      i += 4;
      recognized = true;
    } else if (channel === 0x04 && type === 0x68) {
      // HUMIDITY: 1 byte, 0.5 % resolution
      air.relativeHumidity = round(bytes[i + 2] / 2, 1);
      hasAir = true;
      i += 3;
      recognized = true;
    } else if (channel === 0x05 && type === 0x00) {
      // WATER LEAK: status byte (0 = normal, otherwise leak)
      water.leak = bytes[i + 2] !== 0;
      hasWater = true;
      i += 3;
      recognized = true;
    } else {
      break;
    }
  }

  if (!recognized) {
    return { errors: ['no recognized Milesight channels'] };
  }

  if (hasAir) {
    data.air = air;
  }
  if (hasWater) {
    data.water = water;
  }

  return { data: data };
}

// Device identity (make/model), emitted on every successful decode. See AUTHORING.md.
function decodeUplink(input) {
  var result = decodeUplinkCore(input);
  if (result && result.data) {
    result.data.make = "milesight-iot";
    result.data.model = "em300-sld";
  }
  return result;
}
